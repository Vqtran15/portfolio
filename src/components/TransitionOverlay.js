const TransitionOverlay = () => {
  const ctrl     = useAnimation()
  const busy     = useRef(false)
  const blobRefs = useRef([])

  useEffect(() => {
    ctrl.set({ scale: 0 })

    const handler = async (e) => {
      if (busy.current) return
      busy.current = true

      const { targetId } = e.detail
      const hscroll = document.getElementById('h-scroll')
      const target  = document.getElementById(targetId)

      const palette = PALETTES[targetId] || PALETTES.home
      blobRefs.current.forEach((el, i) => {
        if (el) el.style.background = palette[i]
      })

      await ctrl.start((i) => ({
        scale: 1,
        rotate: BLOBS[i].rot,
        transition: { type: 'spring', stiffness: 500, damping: 28, delay: BLOBS[i].delay },
      }))

      if (hscroll && target) {
        hscroll.style.scrollBehavior = 'auto'
        hscroll.scrollLeft = target.offsetLeft
        hscroll.style.scrollBehavior = ''
      }

      await ctrl.start((i) => ({
        scale: 0,
        rotate: BLOBS[i].rot,
        transition: { duration: 0.06, ease: 'easeIn', delay: Math.floor(i / 6) * 0.005 },
      }))

      busy.current = false
    }

    window.addEventListener('section-navigate', handler)
    return () => window.removeEventListener('section-navigate', handler)
  }, [ctrl])

  return (
    <>
      {BLOBS.map((blob, i) => (
        <motion.div
          key={i}
          custom={i}
          animate={ctrl}
          ref={el => { blobRefs.current[i] = el }}
          initial={{ scale: 0, rotate: blob.rot }}
          style={{
            position: 'fixed',
            left: `calc(${blob.x}% - ${blob.w / 2}vw)`,
            top: `calc(${blob.y}% - ${blob.h / 2}vh)`,
            width: `${blob.w}vw`,
            height: `${blob.h}vh`,
            background: PALETTES.home[i],
            borderRadius: RADII[i],
            originX: blob.ox,
            originY: blob.oy,
            zIndex: 9000,
            pointerEvents: 'none',
            willChange: 'transform',
          }}
        />
      ))}
    </>
  )
}

export default TransitionOverlay
